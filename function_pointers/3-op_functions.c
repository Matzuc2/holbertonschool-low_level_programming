#include "3-calc.h"
/**
 * op_add - addition function
 * @a: int a
 * @b: int b
 * Return: return a operating b
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - function
 * @a: int a
 * @b: int b
 * Return: return a operating b
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - * function
 * @a: int a
 * @b: int b
 * Return: return a operating b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - / function
 * @a: int a
 * @b: int b
 * Return: return a operating b
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - % addition function
 * @a: int a
 * @b: int b
 * Return: return a operating b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
